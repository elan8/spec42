import fs from "node:fs";
import path from "node:path";
import process from "node:process";

const repoRoot = process.cwd();
const configPath = path.join(repoRoot, "config", "domain-libraries.json");
const generatedTsPath = path.join(
  repoRoot,
  "vscode",
  "src",
  "generated",
  "domainLibrariesDefaults.ts"
);
const check = process.argv.includes("--check");

const config = JSON.parse(fs.readFileSync(configPath, "utf8"));
for (const key of ["version", "repo", "format"]) {
  if (typeof config[key] !== "string" || config[key].trim() === "") {
    throw new Error(`config/domain-libraries.json must define a non-empty string for "${key}".`);
  }
}
if (typeof config.contentPath !== "string") {
  throw new Error('config/domain-libraries.json must define "contentPath" as a string.');
}

const generatedTs = `// GENERATED by scripts/sync-domain-libraries-config.mjs — do not edit.
export const DOMAIN_LIBRARIES_DEFAULTS = {
  version: ${JSON.stringify(config.version)},
  repo: ${JSON.stringify(config.repo)},
  format: ${JSON.stringify(config.format)},
  artifact: ${JSON.stringify(config.artifact ?? "")},
  contentPath: ${JSON.stringify(config.contentPath)},
} as const;
`;

if (check) {
  const currentGenerated = fs.existsSync(generatedTsPath)
    ? fs.readFileSync(generatedTsPath, "utf8")
    : "";
  if (currentGenerated !== generatedTs) {
    throw new Error(
      "vscode/src/generated/domainLibrariesDefaults.ts is out of date. Run `node scripts/sync-domain-libraries-config.mjs`."
    );
  }
} else {
  fs.mkdirSync(path.dirname(generatedTsPath), { recursive: true });
  fs.writeFileSync(generatedTsPath, generatedTs, "utf8");
}
