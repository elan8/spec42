import fs from "node:fs";
import path from "node:path";
import process from "node:process";

const repoRoot = process.cwd();
const metadataPath = path.join(repoRoot, "docs", "conformance-metadata.json");
const constantsPath = path.join(repoRoot, "vscode", "src", "visualization", "webview", "constants.ts");
const outputPath = path.join(repoRoot, "docs", "CONFORMANCE-MATRIX.md");
const check = process.argv.includes("--check");

const metadata = JSON.parse(fs.readFileSync(metadataPath, "utf8"));
const constants = fs.readFileSync(constantsPath, "utf8");
const shippedViews = [...constants.matchAll(/SYSML_ENABLED_VIEWS\s*=\s*\[([\s\S]*?)\]/g)]
  .flatMap((match) => [...match[1].matchAll(/'([^']+)'/g)].map((id) => id[1]));
const documentedViews = new Set(metadata.views.map((view) => view.id));
const missingViews = shippedViews.filter((view) => !documentedViews.has(view));
if (missingViews.length > 0) {
  throw new Error(`CONFORMANCE-MATRIX metadata is missing shipped view IDs: ${missingViews.join(", ")}`);
}
if (!Array.isArray(metadata.semanticValidation) || metadata.semanticValidation.length === 0) {
  throw new Error("CONFORMANCE-MATRIX metadata must include semantic validation categories.");
}
for (const category of metadata.semanticValidation) {
  if (!category.category || !Array.isArray(category.codes) || category.codes.length === 0) {
    throw new Error("Each semantic validation category must include a category name and diagnostic codes.");
  }
}

const lines = [];
lines.push("# Spec42 Conformance Matrix", "");
lines.push("Generated from `docs/conformance-metadata.json`. Do not edit this file by hand; run `node scripts/generate-conformance-matrix.mjs`.", "");
section("Language Coverage", ["Area", "Status", "Notes"], metadata.languageCoverage.map((row) => [row.area, row.status, row.notes]));
section("Semantic Validation", ["Category", "Status", "Diagnostic codes"], metadata.semanticValidation.map((row) => [row.category, row.status, row.codes.join(", ")]));
section("View And Rendering Coverage", ["View", "Renderer", "Status", "Notes"], metadata.views.map((row) => [row.id, row.renderer, row.status, row.notes]));
section("CLI And Reporting", ["Feature", "Status"], metadata.cliReporting.map((row) => [row.feature, row.status]));
section("Sysand Integration", ["Feature", "Status"], metadata.sysand.map((row) => [row.feature, row.status]));
lines.push("");

const next = `${lines.join("\n")}\n`;
if (check) {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
  if (current !== next) {
    throw new Error("docs/CONFORMANCE-MATRIX.md is out of date. Run `node scripts/generate-conformance-matrix.mjs`.");
  }
} else {
  fs.writeFileSync(outputPath, next, "utf8");
}

function section(title, headers, rows) {
  lines.push(`## ${title}`, "");
  lines.push(`| ${headers.join(" | ")} |`);
  lines.push(`| ${headers.map(() => "---").join(" | ")} |`);
  for (const row of rows) {
    lines.push(`| ${row.map(cell).join(" | ")} |`);
  }
  lines.push("");
}

function cell(value) {
  return String(value ?? "").replaceAll("|", "\\|");
}
